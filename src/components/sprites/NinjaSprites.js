class NinjaSprites {
    constructor(
        color,
        front,
        down,
        right,
        left,
        up,
        upRight,
        upLeft,
        downRight,
        downLeft
    ) {
        this.color = color;
        this.front = front;
        this.down = down;
        this.right = right;
        this.left = left;
        this.up = up;
        this.upRight = upRight;
        this.upLeft = upLeft;
        this.downRight = downRight;
        this.downLeft = downLeft;
    }
}

export default NinjaSprites;
